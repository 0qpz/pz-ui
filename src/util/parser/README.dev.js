// 2. 性能铁律
// ============================================================================
/*
 ✅ 必须遵守：
 
 1. 快速失败原则
    - 第一时间检查前缀，不匹配立即返回 null
    - 避免无效计算
 
 2. 预编译原则
    - 正则表达式必须在函数外预编译
    - 配置常量使用 const
 
 3. 纯函数原则
    - 无副作用（不修改外部状态）
    - 相同输入永远得到相同输出
    - 便于缓存和优化
 
 4. 返回值规范
    - 匹配：返回 CSS 对象 { 'property': 'value' }
    - 不匹配：返回 null（不是空对象）
 
 5. 命名规范
    - 函数名：parseXxx（驼峰式）
    - 文件名：xxx.js（小写）
    - 常量：大写 + 下划线
 */



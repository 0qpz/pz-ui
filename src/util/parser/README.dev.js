export function parseXxx(input) {
    // 【快速失败】前缀检查
    if (!input || !input.startsWith('xxx-')) {
        return null;
    }
    
    // 提取值部分
    const value = input.substring(4);
    
    // 使用预编译正则进行匹配
    if (SOME_REGEX.test(value)) {
        return { 'property': value };
    }
    
    // 默认情况
    return { 'property': value };
}

